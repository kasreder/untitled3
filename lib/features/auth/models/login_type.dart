// 파일 경로: lib/features/auth/models/login_type.dart
// 파일 설명: 플랫폼에서 지원하는 인증 제공자를 정의.

/// 회원 계정이 사용할 수 있는 로그인 채널을 열거한다.
///
/// 값은 회원 프로필과 함께 저장되어 가입 시 어떤 방식으로 인증했는지 나타낸다.
/// 카카오나 네이버처럼 외부 서비스를 사용할 경우 참조 토큰만 보관하고,
/// 민감한 프로필 데이터는 저장 전에 암호화한다.
enum LoginType {
  /// 플랫폼이 암호화된 자격 증명으로 처리하는 로컬 인증.
  local,

  /// 인증 코드를 토큰으로 교환하는 카카오 소셜 로그인 흐름.
  kakao,

  /// 카카오와 유사한 OAuth 과정을 따르는 네이버 소셜 로그인 흐름.
  naver,
}
